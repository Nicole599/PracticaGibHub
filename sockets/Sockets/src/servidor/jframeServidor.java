    private void jbtIniciarServidorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtIniciarServidorActionPerformed
        // TODO add your handling code here:
        try {
        String textoservidor2=jtfEnviarS.getText().toString();
        
        
            //servidor
            ServerSocket servidor = new ServerSocket(5000);
            Socket clienteNuevo= servidor.accept();
            ObjectInputStream entrada= new ObjectInputStream(clienteNuevo.getInputStream());
            
            //Esperando el mensaje 
            String mensaje = (String) entrada.readObject();
            jtfRecibidoS.setText(mensaje);
            
            //envio de respuesta
            
            ObjectOutputStream respuesta = new ObjectOutputStream(clienteNuevo.getOutputStream());
            respuesta.writeObject(textoservidor2);
            clienteNuevo.close();
            servidor.close();
            
            
        } catch (IOException ex) {
            Logger.getLogger(jframeServidor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(jframeServidor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbtIniciarServidorActionPerformed

